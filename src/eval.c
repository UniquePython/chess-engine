static const int *TABLES[7] = {
    NULL, // NO_PIECE
    PAWN_TABLE,
    KNIGHT_TABLE,
    BISHOP_TABLE,
    ROOK_TABLE,
    QUEEN_TABLE,
    KING_TABLE,
};

static int table_index(Loc l, PC colour)
{
    // tables are stored rank 8 down to rank 1 for white
    // black mirrors vertically
    int rank = (colour == WHITE) ? (7 - l.rank) : l.rank;
    return rank * 8 + l.file;
}

int evaluate(Game *g)
{
    int score = 0;

    for (int i = 0; i < 64; i++)
    {
        Piece p = g->board.squares[i];
        if (is_empty(p))
            continue;

        Loc l = loc_of(i);
        int material = MATERIAL[p.type];
        int positional = TABLES[p.type][table_index(l, p.colour)];
        int value = material + positional;

        if (p.colour == WHITE)
            score += value;
        else
            score -= value;
    }

    return score;
}
